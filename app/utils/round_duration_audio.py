def round_duration(duration: int) -> int:
    """
    Округляет длительность аудио до минут кратно:
    - Если меньше 60 сек → 60 сек (1 минута)
    - Если больше → округление вверх до кратного 60
    Примеры:
    - 12 сек → 60 сек
    - 33 сек → 60 сек
    - 65 сек → 120 сек (2 минуты)
    - 150 сек → 180 сек (3 минуты)
    """
    if duration < 60:
        return 60
    return ((duration + 59) // 60) * 60
